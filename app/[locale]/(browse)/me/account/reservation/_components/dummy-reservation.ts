interface LibraryItem {
  id: string
  title: string
  author: string
  type: "book" | "ebook" | "audiobook" | "journal" | "magazine" | "dvd"
  coverImage: string
  publishedYear: number
  publisher: string
  isbn?: string
  description: string
  categories: string[]
  available: boolean
  totalCopies: number
  availableCopies: number
  location: string
  popularity: number // 1-10 scale
  language: string
  pages?: number
  duration?: string // for audiobooks
}


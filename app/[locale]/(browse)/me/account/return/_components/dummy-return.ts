export type ItemType =
  | "book"
  | "ebook"
  | "audiobook"
  | "journal"
  | "magazine"
  | "dvd"
export type BorrowStatus = "borrowed" | "overdue" | "returned" | "lost"
export type ReturnCondition = "excellent" | "good" | "fair" | "damaged" | "lost"
export interface ReturnHistory {
  id: string
  borrowId: string
  returnDate: string
  condition: ReturnCondition
  notes?: string
  processedBy: string
  lateFee: number
  paid: boolean
  borrowedItem: BorrowedItem
}
interface LibraryItem {
  id: string
  title: string
  author: string
  type: ItemType
  coverImage: string
  publishedYear: number
  publisher: string
  isbn?: string
  description: string
  categories: string[]
  location: string
  language: string
  pages?: number
  duration?: string // for audiobooks
}

export interface BorrowedItem {
  id: string
  itemId: string
  userId: string
  borrowDate: string
  dueDate: string
  returnDate?: string
  status: BorrowStatus
  renewalCount: number
  condition: ReturnCondition | null
  notes?: string
  lateFee: number
  item: LibraryItem
}

